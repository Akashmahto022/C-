#include <iostream>
using namespace std;

int main(){
    int first = 50;
    int second = 50;
    int total = first + second;

    cout << first << "+" << second << "=" << total << endl;
    cout << "sum " << (first + second) << endl;
    cout << "difference " << (first - second) << endl;
    cout << "product " << (first * second) << endl;
    cout << "division " << (first / second) << endl;
    cout << "modulo " << (first % second) << endl;
}
